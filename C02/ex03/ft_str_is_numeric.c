int ft_str_is_numeric(char *str)
{
	int	a;
	int	b;

	a = 1;
	b = 0;
	while (str[b] != '\0')
	{
		if (str[b] < 48 || str[b] > 57)
		{
			a = 0;
		}
		b++;
	}
	return (a);
}
