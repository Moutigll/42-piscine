int	ft_str_is_printable(char *str)
{
	int	a;
	int	b;

	a = 1;
	b = 0;
	while (str[b] != '\0')
	{
		if (str[b] < 32 || str[b] > 127)
		{
			a = 0;
		}
		b++;
	}
	return (a);
}
