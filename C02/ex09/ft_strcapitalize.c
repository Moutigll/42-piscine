char	*ft_strcapitalize(char *str)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[a] < 97 || str[a] > 122)
		{
			if (str[a + 1] > 96 && str[a + 1] < 123)
			{
				str[a + 1] -= 32;
			}
		}
		a++;
	}
	return (str);
}
