int	ft_str_is_alpha(char *str)
{
	int	a;
	int	b;

	a = 1;
	b = 0;
	while (str[b] != '\0')
	{
		if (str[b] < 65 || str[b] > 123 || (str[b] > 90 && str[b] < 97))
		{
			a = 0;
		}
		b++;
	}
	return (a);
}
