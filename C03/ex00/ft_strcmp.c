int	ft_strcmp(char *s1, char *s2)
{
	int	c[4];

	c[0] = 0;
	c[1] = 0;
	c[2] = 0;
	c[3] = 0;
	while (s1[c[0]] != '\0' && s1[c[1]] != '\0')
	{
		if (s1[c[0]] != '\0')
		{
			c[0]++;
			c[2]++;
		}
		if (s2[c[1]] != '\0')
		{
			c[1]++;
			c[3]++;
		}
	}
	return (c[2] - c[3]);
}
